#!/usr/bin/env bun

/**
 * LiteLLM 价格表 -> TOML（云端基础价格表）
 *
 * 用法:
 *   bun run scripts/convert-litellm-to-toml.ts
 *
 * 输入:
 *   public/seed/litellm-prices.json
 *
 * 输出:
 *   ../claude-code-hub-docs/public/config/prices-base.toml
 *
 * 筛选:
 *   - model name 前缀: claude, gpt, o1, o3, gemini
 *   - 仅保留 mode === "chat"
 */

import { createHash } from "node:crypto";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

export const DEFAULT_PREFIXES = ["claude", "gpt", "o1", "o3", "gemini"] as const;

type PlainObject = Record<string, unknown>;

function isPlainObject(value: unknown): value is PlainObject {
  return typeof value === "object" && value !== null && !Array.isArray(value);
}

function isTargetModelName(name: string, prefixes: readonly string[]): boolean {
  const lower = name.toLowerCase();
  return prefixes.some((prefix) => lower.startsWith(prefix.toLowerCase()));
}

export function filterLiteLLMPrices(
  input: Record<string, unknown>,
  prefixes: readonly string[] = DEFAULT_PREFIXES,
): Record<string, PlainObject> {
  const filtered: Record<string, PlainObject> = {};

  for (const [name, spec] of Object.entries(input)) {
    if (name === "sample_spec") continue;
    if (!isTargetModelName(name, prefixes)) continue;
    if (!isPlainObject(spec)) continue;
    if (spec.mode !== "chat") continue;
    filtered[name] = spec;
  }

  return filtered;
}

export function calculateModelsChecksum(models: Record<string, unknown>): string {
  const sorted = Object.fromEntries(Object.keys(models).sort().map((k) => [k, models[k]]));
  return createHash("sha256").update(JSON.stringify(sorted)).digest("hex");
}

function isBareKey(key: string): boolean {
  return /^[A-Za-z0-9_-]+$/.test(key);
}

function formatKeySegment(key: string, forceQuote = false): string {
  if (forceQuote) return JSON.stringify(key);
  return isBareKey(key) ? key : JSON.stringify(key);
}

function formatTomlValue(value: unknown): string {
  if (typeof value === "string") return JSON.stringify(value);
  if (typeof value === "number") {
    if (!Number.isFinite(value)) throw new Error(`Unsupported number value: ${String(value)}`);
    return String(value);
  }
  if (typeof value === "boolean") return value ? "true" : "false";
  if (Array.isArray(value)) {
    const parts = value.map((item) => {
      if (typeof item === "string") return JSON.stringify(item);
      if (typeof item === "number") {
        if (!Number.isFinite(item)) throw new Error(`Unsupported number value in array: ${String(item)}`);
        return String(item);
      }
      if (typeof item === "boolean") return item ? "true" : "false";
      throw new Error(`Unsupported array item type: ${typeof item}`);
    });
    return `[${parts.join(", ")}]`;
  }
  if (value === null) throw new Error("TOML does not support null values");
  throw new Error(`Unsupported value type: ${typeof value}`);
}

function writeTomlTable(lines: string[], segments: string[], data: PlainObject): void {
  const header = `[${segments.join(".")}]`;
  lines.push(header);

  const primitiveEntries: Array<[string, unknown]> = [];
  const objectEntries: Array<[string, PlainObject]> = [];

  for (const [key, value] of Object.entries(data)) {
    if (value === undefined) continue;
    if (isPlainObject(value)) objectEntries.push([key, value]);
    else primitiveEntries.push([key, value]);
  }

  primitiveEntries.sort(([a], [b]) => a.localeCompare(b));
  objectEntries.sort(([a], [b]) => a.localeCompare(b));

  for (const [key, value] of primitiveEntries) {
    lines.push(`${formatKeySegment(key)} = ${formatTomlValue(value)}`);
  }

  for (const [key, value] of objectEntries) {
    lines.push("");
    writeTomlTable(lines, [...segments, formatKeySegment(key)], value);
  }
}

export function buildPricesBaseToml(args: {
  models: Record<string, unknown>;
  version: string;
}): string {
  const sortedModelNames = Object.keys(args.models).sort();
  const sortedModels = Object.fromEntries(sortedModelNames.map((name) => [name, args.models[name]]));
  const checksum = calculateModelsChecksum(sortedModels);

  const lines: string[] = [];
  lines.push("# Generated by scripts/convert-litellm-to-toml.ts");
  lines.push("");
  lines.push("[metadata]");
  lines.push(`version = ${JSON.stringify(args.version)}`);
  lines.push(`checksum = ${JSON.stringify(checksum)}`);
  lines.push("");

  for (const modelName of sortedModelNames) {
    const rawSpec = args.models[modelName];
    if (!isPlainObject(rawSpec)) continue;

    const segments = [formatKeySegment("models"), formatKeySegment(modelName, true)];
    writeTomlTable(lines, segments, rawSpec);
    lines.push("");
  }

  return `${lines.join("\n").trimEnd()}\n`;
}

function formatVersion(date: Date): string {
  const yyyy = String(date.getFullYear());
  const mm = String(date.getMonth() + 1).padStart(2, "0");
  const dd = String(date.getDate()).padStart(2, "0");
  return `${yyyy}.${mm}.${dd}`;
}

export function getDefaultPaths(): { inputPath: string; outputPath: string } {
  const currentFile = fileURLToPath(import.meta.url);
  const repoRoot = path.resolve(path.dirname(currentFile), "..");

  return {
    inputPath: path.resolve(repoRoot, "public/seed/litellm-prices.json"),
    outputPath: path.resolve(repoRoot, "../claude-code-hub-docs/public/config/prices-base.toml"),
  };
}

export async function convertLiteLLMPricesToTomlFile(args?: {
  inputPath?: string;
  outputPath?: string;
  version?: string;
}): Promise<{ modelCount: number; version: string; checksum: string; outputPath: string }> {
  const defaults = getDefaultPaths();
  const inputPath = args?.inputPath ?? defaults.inputPath;
  const outputPath = args?.outputPath ?? defaults.outputPath;
  const version = args?.version ?? formatVersion(new Date());

  const raw = await readFile(inputPath, "utf8");
  const json = JSON.parse(raw) as Record<string, unknown>;

  const filtered = filterLiteLLMPrices(json, DEFAULT_PREFIXES);
  const modelCount = Object.keys(filtered).length;

  const toml = buildPricesBaseToml({ models: filtered, version });
  const checksum = calculateModelsChecksum(filtered);

  await mkdir(path.dirname(outputPath), { recursive: true });
  await writeFile(outputPath, toml, "utf8");

  return { modelCount, version, checksum, outputPath };
}

async function main(): Promise<void> {
  const { modelCount, outputPath, version, checksum } = await convertLiteLLMPricesToTomlFile();
  console.log(
    JSON.stringify(
      {
        ok: true,
        modelCount,
        version,
        checksum,
        outputPath,
      },
      null,
      2,
    ),
  );
}

if (import.meta.main) {
  main().catch((err) => {
    console.error(err);
    process.exitCode = 1;
  });
}
